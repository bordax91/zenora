import { NextResponse } from 'next/server'
import { createClient } from '@supabase/supabase-js'
import stripe from '@/lib/stripe'
import { sendClientConfirmationEmail } from '@/app/api/emails/send-confirmation-email/route'
import { sendCoachConfirmationEmail } from '@/app/api/emails/send-confirmation-email/route'

const secretMain = process.env.STRIPE_WEBHOOK_SECRET_MAIN
const secretConnected = process.env.STRIPE_WEBHOOK_SECRET_CONNECTED

const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.SUPABASE_SERVICE_ROLE_KEY
)

export async function POST(req) {
  const sig = req.headers.get('stripe-signature')
  if (!sig) {
    return NextResponse.json({ error: 'Signature manquante' }, { status: 400 })
  }

  const rawBody = await req.text()
  let event = null

  // Essayer avec les deux secrets (connect√© ou principal)
  const secrets = [
    { name: 'CONNECTED', key: secretConnected },
    { name: 'MAIN', key: secretMain }
  ]

  for (const { name, key } of secrets) {
    try {
      event = stripe.webhooks.constructEvent(rawBody, sig, key)
      console.log(`‚úÖ Webhook re√ßu (${name}) :`, event.type)
      break
    } catch (err) {
      console.warn(`‚ùå Signature invalide pour secret ${name}`)
    }
  }

  if (!event) {
    return NextResponse.json({ error: 'Signature invalide pour tous les secrets' }, { status: 400 })
  }

  const session = event.data.object
  const metadata = session.metadata || {}

  // üéØ CAS 1 : Paiement de session client (compte connect√©)
  if (
    event.type === 'checkout.session.completed' &&
    metadata.client_id && metadata.package_id && metadata.availability_id
  ) {
    const { client_id: clientId, package_id: packageId, availability_id: availabilityId } = metadata

    // üîé V√©rifier que le client existe
    const { data: client, error: clientError } = await supabase
      .from('users')
      .select('name, email')
      .eq('id', clientId)
      .single()

    if (clientError || !client) {
      console.error('‚ùå Client introuvable :', clientError)
      return NextResponse.json({ error: 'Client introuvable' }, { status: 404 })
    }

    // üîé V√©rifier que le cr√©neau est valide et dispo
    const { data: availability, error: availabilityError } = await supabase
      .from('availabilities')
      .select('date, coach_id, is_booked')
      .eq('id', availabilityId)
      .single()

    if (availabilityError || !availability) {
      console.error('‚ùå Cr√©neau non trouv√© :', availabilityError)
      return NextResponse.json({ error: 'Cr√©neau non trouv√©' }, { status: 404 })
    }

    if (availability.is_booked) {
      console.warn('‚ö†Ô∏è Cr√©neau d√©j√† r√©serv√©')
      return NextResponse.json({ message: 'Cr√©neau d√©j√† r√©serv√©' }, { status: 200 })
    }

    const { coach_id, date } = availability

    // üîé V√©rifier que le coach existe
    const { data: coach, error: coachError } = await supabase
      .from('users')
      .select('name, email')
      .eq('id', coach_id)
      .single()

    if (coachError || !coach) {
      console.error('‚ùå Coach introuvable :', coachError)
      return NextResponse.json({ error: 'Coach introuvable' }, { status: 404 })
    }

    // üîé V√©rifier que le package existe
    const { data: packageData, error: packageError } = await supabase
      .from('packages')
      .select('title')
      .eq('id', packageId)
      .single()

    if (packageError || !packageData) {
      console.error('‚ùå Package introuvable :', packageError)
      return NextResponse.json({ error: 'Offre introuvable' }, { status: 404 })
    }

    // ‚úÖ Ins√©rer la session
    const { error: insertError } = await supabase.from('sessions').insert({
      coach_id,
      client_id: clientId,
      package_id: packageId,
      date,
      availability_id: availabilityId,
      statut: 'r√©serv√©'
    })

    if (insertError) {
      console.error('‚ùå Erreur insertion session :', insertError)
      return NextResponse.json({ error: 'Impossible de cr√©er la session' }, { status: 500 })
    }

    // ‚úÖ Marquer le cr√©neau comme r√©serv√©
    await supabase
      .from('availabilities')
      .update({ is_booked: true })
      .eq('id', availabilityId)

    // ‚úÖ Envoyer les emails
    try {
      await sendClientConfirmationEmail({
        to: client.email,
        clientName: client.name,
        coachName: coach.name,
        date,
        time: new Date(date).toLocaleTimeString(),
        packageTitle: packageData.title
      })

      await sendCoachConfirmationEmail({
        to: coach.email,
        coachName: coach.name,
        clientName: client.name,
        date,
        time: new Date(date).toLocaleTimeString(),
        packageTitle: packageData.title
      })
    } catch (emailErr) {
      console.error('‚ùå Erreur envoi email confirmation :', emailErr)
    }

    console.log('‚úÖ Session cr√©√©e + cr√©neau r√©serv√© + emails envoy√©s')
  }

  // üéØ CAS 2 : Paiement abonnement coach (compte principal)
  if (
    event.type === 'checkout.session.completed' &&
    session.mode === 'subscription'
  ) {
    const stripeCustomerId = session.customer
    const coachId = metadata.coach_id || metadata.user_id || null
    const customerEmail = session.customer_email || null

    const subscription = await stripe.subscriptions.retrieve(session.subscription)
    const priceId = subscription?.items?.data?.[0]?.price?.id || null

    let subscriptionType = 'inconnu'
    if (priceId === process.env.STRIPE_PRICE_ID_MONTHLY) {
      subscriptionType = 'mensuel'
    } else if (priceId === process.env.STRIPE_PRICE_ID_YEARLY) {
      subscriptionType = 'annuel'
    }

    let updateError

    if (coachId) {
      ({ error: updateError } = await supabase
        .from('users')
        .update({
          is_subscribed: true,
          stripe_customer_id: stripeCustomerId,
          subscription_started_at: new Date().toISOString(),
          subscription_type: subscriptionType
        })
        .eq('id', coachId))
    } else if (customerEmail) {
      ({ error: updateError } = await supabase
        .from('users')
        .update({
          is_subscribed: true,
          stripe_customer_id: stripeCustomerId,
          subscription_started_at: new Date().toISOString(),
          subscription_type: subscriptionType
        })
        .eq('email', customerEmail))
    }

    if (updateError) {
      console.error('‚ùå Erreur MAJ abonnement coach :', updateError)
    } else {
      console.log(`‚úÖ Abonnement ${subscriptionType} activ√© pour coach via ${coachId || customerEmail}`)
    }
  }

  // üéØ CAS 3 : D√©sabonnement
  if (event.type === 'customer.subscription.deleted') {
    const customerId = event.data.object.customer

    const { error: unsubError } = await supabase
      .from('users')
      .update({ is_subscribed: false })
      .eq('stripe_customer_id', customerId)

    if (unsubError) {
      console.error('‚ùå Erreur d√©sabonnement coach :', unsubError)
    } else {
      console.log(`üö´ Coach d√©sabonn√© (customer ${customerId})`)
    }
  }

  return NextResponse.json({ received: true }, { status: 200 })
}
